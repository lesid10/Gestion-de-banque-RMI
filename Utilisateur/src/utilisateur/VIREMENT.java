    private void Bt_montantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_montantActionPerformed
          if(Txtbox_montant.getText().equals("") || Integer.valueOf(Txtbox_montant.getText()).intValue()<0 || Txtbox_NumCpteDest.getText().equals("") ){
            JOptionPane.showMessageDialog(null,"Donnez toutes les informations svp!!!","INFO",JOptionPane.INFORMATION_MESSAGE);
     }else{
           try {
             IOperation  J = (IOperation) Naming.lookup("rmi://localhost:1099/BANQUE");
             J.Virement(this.numcpte, Txtbox_NumCpteDest.getText(), Integer.valueOf(Txtbox_montant.getText()).intValue());
             JOptionPane.showMessageDialog(null,"Virement effectué avec succès","VIREMENT",JOptionPane.INFORMATION_MESSAGE);
             this.dispose();
        }catch (RemoteException ex4) {
        JOptionPane.showMessageDialog(null,"La connexion au serveur a échoué","Serveur non démarré",JOptionPane.WARNING_MESSAGE);
       }
       catch (MalformedURLException ex4) {
            JOptionPane.showMessageDialog(null,"La connexion au serveur a échoué","Erreur de connexion",JOptionPane.WARNING_MESSAGE);
       }
       catch (NotBoundException ex4) {
             JOptionPane.showMessageDialog(null,"Serveur non joignable","Erreur de connexion",JOptionPane.WARNING_MESSAGE);
        } 
    }
    }//GEN-LAST:event_Bt_montantActionPerformed

